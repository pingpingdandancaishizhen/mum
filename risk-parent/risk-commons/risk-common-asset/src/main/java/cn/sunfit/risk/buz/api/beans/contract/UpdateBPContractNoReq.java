package cn.sunfit.risk.buz.api.beans.contract;

import orj.worf.core.model.BaseObject;

public class UpdateBPContractNoReq extends BaseObject {

    /**
     * 
     */
    private static final long serialVersionUID = 28213276170721447L;

    private String domain;

    private String bpId;

    private String contractNo;

    public UpdateBPContractNoReq() {

    }

    public UpdateBPContractNoReq(String domain, String bpId, String contractNo) {
        this.domain = domain;
        this.bpId = bpId;
        this.contractNo = contractNo;
    }

    public String getBpId() {
        return bpId;
    }

    public String getContractNo() {
        return contractNo;
    }

    public String getDomain() {
        return domain;
    }

    public void setBpId(String bpId) {
        this.bpId = bpId;
    }

    public void setContractNo(String contractNo) {
        this.contractNo = contractNo;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

}
