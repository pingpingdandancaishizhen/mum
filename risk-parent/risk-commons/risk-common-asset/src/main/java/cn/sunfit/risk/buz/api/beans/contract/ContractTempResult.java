package cn.sunfit.risk.buz.api.beans.contract;

import java.util.List;

import orj.worf.core.model.BaseObject;
import cn.sunfit.risk.buz.api.vo.product.ProductContractFields;

public class ContractTempResult extends BaseObject {

    /**
     * 
     */
    private static final long serialVersionUID = -5678791624628765100L;

    private List<String> tempFields;

    private List<ProductContractFields> dataFields;

    public ContractTempResult() {

    }

    public ContractTempResult(List<String> tempFields, List<ProductContractFields> dataFields) {
        this.tempFields = tempFields;
        this.dataFields = dataFields;
    }

    public List<ProductContractFields> getDataFields() {
        return dataFields;
    }

    public List<String> getTempFields() {
        return tempFields;
    }

    public void setDataFields(List<ProductContractFields> dataFields) {
        this.dataFields = dataFields;
    }

    public void setTempFields(List<String> tempFields) {
        this.tempFields = tempFields;
    }

}
