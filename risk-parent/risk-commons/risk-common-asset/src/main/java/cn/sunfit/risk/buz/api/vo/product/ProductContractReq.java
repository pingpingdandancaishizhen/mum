package cn.sunfit.risk.buz.api.vo.product;

import cn.sunfit.risk.buz.api.vo.CorpReqBase;

public class ProductContractReq extends CorpReqBase {

    private static final long serialVersionUID = 1L;

    private String domain;

    private String bpId;

    private String productId;

    public ProductContractReq() {

    }

    public ProductContractReq(String domain, String bpId, String productId) {
        this.bpId = bpId;
        this.productId = productId;
        this.domain = domain;
    }

    public String getBpId() {
        return bpId;
    }

    @Override
    public String getDomain() {
        return domain;
    }

    public String getProductId() {
        return productId;
    }

    public void setBpId(String bpId) {
        this.bpId = bpId;
    }

    @Override
    public void setDomain(String domain) {
        this.domain = domain;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

}
