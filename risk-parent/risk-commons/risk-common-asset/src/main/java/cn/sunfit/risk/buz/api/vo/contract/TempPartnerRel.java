package cn.sunfit.risk.buz.api.vo.contract;

import orj.worf.core.model.BaseObject;

public class TempPartnerRel extends BaseObject {

    /**
     * 
     */
    private static final long serialVersionUID = 668347118695234451L;

    private String tempId;

    private String partnerId;

    private String roleId;

    public TempPartnerRel() {

    }

    public TempPartnerRel(String tempId, String partnerId, String roleId) {
        this.tempId = tempId;
        this.partnerId = partnerId;
        this.roleId = roleId;
    }

    public String getPartnerId() {
        return partnerId;
    }

    public String getRoleId() {
        return roleId;
    }

    public String getTempId() {
        return tempId;
    }

    public void setPartnerId(String partnerId) {
        this.partnerId = partnerId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    public void setTempId(String tempId) {
        this.tempId = tempId;
    }

}
