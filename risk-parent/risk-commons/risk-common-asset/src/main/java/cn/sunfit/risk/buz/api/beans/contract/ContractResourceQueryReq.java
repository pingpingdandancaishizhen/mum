package cn.sunfit.risk.buz.api.beans.contract;

import cn.sunfit.risk.buz.api.vo.CorpReqBase;

public class ContractResourceQueryReq extends CorpReqBase {

    /**
     * 
     */
    private static final long serialVersionUID = 2471350859393442150L;

    private String bpId;

    private String tempId;

    private String contractType;

    public ContractResourceQueryReq() {

    }

    public ContractResourceQueryReq(String bpId, String tempId, String contractType) {
        this.bpId = bpId;
        this.tempId = tempId;
        this.contractType = contractType;
    }

    public String getBpId() {
        return bpId;
    }

    public String getContractType() {
        return contractType;
    }

    public String getTempId() {
        return tempId;
    }

    public void setBpId(String bpId) {
        this.bpId = bpId;
    }

    public void setContractType(String contractType) {
        this.contractType = contractType;
    }

    public void setTempId(String tempId) {
        this.tempId = tempId;
    }

}
